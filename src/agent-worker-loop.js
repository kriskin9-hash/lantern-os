/**
 * Agent Worker Loop — Phase 5
 *
 * Orchestrates the full lifecycle of one unit of work:
 *   dispatch → spawn agent → validate → commit → PR → complete
 *
 * The "spawn agent" step invokes `claude` CLI (Claude Code) with the
 * work context as a prompt. On non-Claude lanes (or when CLI is absent)
 * it falls back to a no-op stub so the rest of the pipeline still runs.
 *
 * Depends on: queue-manager, agent-slot-manager, work-dispatcher, worktree-manager
 */

"use strict";

const fs              = require("fs");
const path            = require("path");
const { execSync, spawnSync } = require("child_process");
const QueueManager    = require("./queue-manager");
const AgentSlotManager = require("./agent-slot-manager");
const { dispatchOne } = require("./work-dispatcher");
const { removeWorktree } = require("./worktree-manager");

const REPO_ROOT    = path.resolve(__dirname, "..");
const MAX_AGENT_MS = parseInt(process.env.AGENT_TIMEOUT_MS || "300000", 10);
const _queue       = new QueueManager(path.join(REPO_ROOT, "data", "agent-work-queue"));
const _slots       = new AgentSlotManager();

// ── Git helpers ───────────────────────────────────────────────────────────────

function git(cmd, cwd = REPO_ROOT) {
  return execSync(`git ${cmd}`, { cwd, encoding: "utf8" }).trim();
}

function gitSafe(cmd, cwd = REPO_ROOT) {
  try { return git(cmd, cwd); } catch (e) { return null; }
}

// ── Test runner ───────────────────────────────────────────────────────────────

function runTests(worktreePath) {
  const results = { passed: true, output: "" };
  try {
    const out = execSync(
      "python -m pytest tests/ -q --tb=short " +
        "--ignore=tests/test_anti_entropy_memory.py " +
        "--ignore=tests/test_audit_chain.py " +
        "--ignore=tests/test_discord_bot.py " +
        "--ignore=tests/test_discord_voice_gate.py",
      { cwd: worktreePath, encoding: "utf8", timeout: 120_000 }
    );
    results.output = out.slice(-500);
    results.passed = !out.includes("FAILED") && !out.includes("ERROR");
  } catch (e) {
    results.output = (e.stdout || e.message).slice(-500);
    results.passed = false;
  }
  return results;
}

// ── PR creation ───────────────────────────────────────────────────────────────

function createPR(branch, issueNumber, title) {
  try {
    const body = `Closes #${issueNumber}\n\nGenerated by Claude lane worker loop.\n\n🤖 Generated with [Claude Code](https://claude.com/claude-code)`;
    const out = execSync(
      `gh pr create --repo alex-place/lantern-os --head cdblasioli-gif:${branch} --base master --title ${JSON.stringify(`fix: ${title}`)} --body ${JSON.stringify(body)}`,
      { cwd: REPO_ROOT, encoding: "utf8", timeout: 30_000 }
    ).trim();
    const url = out.match(/https:\/\/github\.com\/\S+/)?.[0] || out;
    return { ok: true, url };
  } catch (e) {
    return { ok: false, error: e.message.slice(0, 300) };
  }
}

// ── Agent invocation ──────────────────────────────────────────────────────────

function spawnClaudeAgent(worktreePath, context) {
  // Check if claude CLI is available
  const whichClaude = spawnSync("where", ["claude"], { encoding: "utf8", shell: true });
  if (whichClaude.status !== 0) {
    return { ok: false, skipped: true, reason: "claude CLI not found — manual work required" };
  }

  const prompt = [
    `You are working on: ${context.title}`,
    `Issue: ${context.issue_url}`,
    "",
    context.body_excerpt || "",
    "",
    "Read the issue, make the necessary code changes, run any tests, and commit your work.",
    "Keep commits small and focused. Do not push — the system will handle that.",
  ].filter(s => s !== undefined).join("\n");

  const result = spawnSync("claude", ["--print", "--dangerously-skip-permissions", prompt], {
    cwd:      worktreePath,
    encoding: "utf8",
    timeout:  MAX_AGENT_MS,
    shell:    true,
  });

  return {
    ok:     result.status === 0,
    stdout: (result.stdout || "").slice(0, 2000),
    stderr: (result.stderr || "").slice(0, 500),
    status: result.status,
  };
}

// ── Commit any changes the agent made ────────────────────────────────────────

function commitAgentWork(worktreePath, issueNumber, title) {
  const dirty = gitSafe("status --short", worktreePath);
  if (!dirty || dirty.trim() === "") return { committed: false, reason: "no changes" };

  gitSafe("add -A", worktreePath);
  const msg = `fix: ${title.slice(0, 60)} (issue #${issueNumber})\n\nCo-Authored-By: Claude Sonnet 4.6 <noreply@anthropic.com>`;
  try {
    git(`commit -m ${JSON.stringify(msg)}`, worktreePath);
    return { committed: true };
  } catch (e) {
    return { committed: false, reason: e.message.slice(0, 200) };
  }
}

function pushBranch(worktreePath, branch) {
  try {
    git(`push origin ${JSON.stringify(branch)}`, worktreePath);
    return { ok: true };
  } catch (e) {
    return { ok: false, error: e.message.slice(0, 200) };
  }
}

// ── Main worker loop ──────────────────────────────────────────────────────────

/**
 * Process one work item end-to-end.
 * Returns a receipt object summarising what happened.
 */
async function processOne(lane, opts = {}) {
  const workItem = await dispatchOne(lane, opts);
  if (!workItem) return null;

  const { entry, slot, worktreePath, branch, context } = workItem;
  const receipt = {
    entry_id:     entry.id,
    issue_number: entry.issue_number,
    slot_id:      slot.id,
    branch,
    worktreePath,
    steps:        [],
    ok:           false,
  };

  const step = (name, result) => {
    receipt.steps.push({ name, ...result });
    try { _slots.heartbeat(slot.id); } catch {}
    return result;
  };

  const issueNum = entry.issueNumber || entry.issue_number;
  const title    = entry.title || `issue #${issueNum}`;

  try {
    // 1. Spawn agent
    const agentResult = step("agent", worktreePath
      ? spawnClaudeAgent(worktreePath, context)
      : { ok: false, skipped: true, reason: "no worktree" });

    if (!agentResult.ok && !agentResult.skipped) {
      step("fail", { ok: false, reason: "agent failed" });
      await _queue.markFailed(entry.id, agentResult.stderr || agentResult.reason || "agent failed");
      try { _slots.completeWork(slot.id, { workId: entry.id, failed: true }); } catch {}
      receipt.ok = false;
      return receipt;
    }

    // 2. Commit changes
    if (worktreePath) {
      const commitResult = step("commit", commitAgentWork(worktreePath, issueNum, title));

      // 3. Run tests
      step("tests", runTests(worktreePath));

      if (commitResult.committed) {
        // 4. Push branch
        const pushResult = step("push", pushBranch(worktreePath, branch));

        // 5. Create PR (only if push succeeded)
        if (pushResult.ok) {
          const prResult = step("pr", createPR(branch, issueNum, title));
          if (prResult.ok) {
            receipt.pr_url = prResult.url;
          }
        }
      }
    }

    // 6. Complete queue entry
    receipt.ok = true;
    await _queue.markComplete(entry.id, receipt);
    try { _slots.completeWork(slot.id, { workId: entry.id, duration: Date.now() - new Date(entry.assignedAt || Date.now()).getTime() }); } catch {}

    // 7. Cleanup worktree
    if (worktreePath && !opts.keepWorktree) {
      try { removeWorktree(worktreePath, { deleteBranch: false }); } catch {}
    }

  } catch (err) {
    step("error", { ok: false, reason: err.message });
    try { await _queue.markFailed(entry.id, err.message); } catch {}
    try { _slots.completeWork(slot.id, { workId: entry.id, failed: true }); } catch {}
  }

  return receipt;
}

/**
 * Run the worker loop continuously until the queue is empty.
 */
async function runLoop(lane, opts = {}) {
  const receipts = [];
  let receipt;
  while ((receipt = await processOne(lane, opts)) !== null) {
    receipts.push(receipt);
    if (opts.onComplete) opts.onComplete(receipt);
  }
  return receipts;
}

module.exports = { processOne, runLoop, runTests, commitAgentWork, createPR };
