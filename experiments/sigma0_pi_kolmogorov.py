"""
π as the Kolmogorov specimen — "looks random, generated by nothing."

This answers "use pi?" two ways, both measured:

  1. π's digits LOOK random (uniform, zstd extracts only the trivial symbol
     entropy and finds no deeper pattern) yet are generated by a ~30-byte
     program. So a generator-aware codec crushes any byte-level codec — the
     cleanest proof that apparent entropy ≠ Kolmogorov complexity.

  2. The seductive inverse — "store arbitrary data as an (offset,length) into
     π" — does NOT compress. The first occurrence of an L-digit string sits, on
     average, ~10^L digits deep, so the offset takes ~L digits to write: you
     spend as much as you saved. This is the counting/no-free-lunch limit, and
     it is the same fact as "a truly incompressible sequence is uncomputable."

Deterministic, pure-Python (Gibbons unbounded spigot), no network. Run:
    python experiments/sigma0_pi_kolmogorov.py
"""
from __future__ import annotations

import json
import math
import os
import sys

_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if _ROOT not in sys.path:
    sys.path.insert(0, _ROOT)

try:
    sys.stdout.reconfigure(encoding="utf-8")
except Exception:  # noqa: BLE001
    pass

import zstandard  # noqa: E402

N_DIGITS = 20000          # π digits to generate (DESIGNED knob)
ZSTD_LEVEL = 19
ARTIFACT = os.path.join("data", "sigma0_pi_kolmogorov_report.json")


def pi_spigot(limit: int) -> list[int]:
    """Gibbons' unbounded spigot — yields the decimal digits of π (3,1,4,1,...).
    THIS function is the whole 'generator': a few lines, a handful of bytes."""
    q, r, t, k, n, l = 1, 0, 1, 1, 3, 3
    out = []
    while len(out) < limit:
        if 4 * q + r - t < n * t:
            out.append(n)
            nr = 10 * (r - n * t)
            n = ((10 * (3 * q + r)) // t) - 10 * n
            q *= 10
            r = nr
        else:
            nr = (2 * q + r) * l
            nn = (q * (7 * k + 2) + r * l) // (t * l)
            q *= k
            t *= l
            l += 2
            k += 1
            n = nn
            r = nr
    return out


def chi_square_uniform(digits: list[int]) -> float:
    """χ² statistic for digit uniformity (10 bins). df=9; ~16.9 = 5% critical."""
    counts = [0] * 10
    for d in digits:
        counts[d] += 1
    exp = len(digits) / 10.0
    return sum((c - exp) ** 2 / exp for c in counts)


def main() -> None:
    digits = pi_spigot(N_DIGITS)
    prefix = "".join(map(str, digits[:15]))
    assert prefix == "314159265358979", f"spigot wrong: {prefix}"

    digit_str = "".join(map(str, digits))
    ascii_bytes = digit_str.encode("ascii")            # 1 byte/digit baseline

    # ── 1. byte-level view: π "looks random" ──
    z = zstandard.ZstdCompressor(level=ZSTD_LEVEL).compress(ascii_bytes)
    entropy_min_bytes = math.ceil(N_DIGITS * math.log2(10) / 8)  # 10-symbol entropy
    chi2 = chi_square_uniform(digits)

    # ── generator-aware (Kolmogorov) codec: store the recipe, re-run to verify ──
    recipe = {"gen": "pi_spigot", "n": N_DIGITS}
    recipe_bytes = len(json.dumps(recipe, separators=(",", ":")).encode("utf-8"))
    lossless = (pi_spigot(recipe["n"]) == digits)

    # ── 2. π-as-universal-store FAILS: the address IS the data ──
    # An L-digit string occurs, on average, ~10^L digits deep. Encoding that
    # offset costs log2(10^L) = L*log2(10) bits — EXACTLY the information content
    # of L decimal digits. The pointer carries as much information as the data it
    # points at. (The finite-window 'first occurrence' is shown too, but it only
    # exists for L ≲ log10(N) and small-L savings are a window artifact, not real.)
    target = "20260616"   # a real date string (digits), DESIGNED but arbitrary
    offsets = []
    for L in range(1, 9):
        needle = target[:L]
        idx = digit_str.find(needle)
        raw_bits = L * math.log2(10)                  # info content of L digits
        expected_offset = 10 ** L                     # avg first-occurrence depth
        expected_offset_bits = math.log2(expected_offset)  # = L*log2(10) == raw_bits
        offsets.append({
            "length_digits": L,
            "raw_data_bits": round(raw_bits, 2),
            "expected_offset_depth": expected_offset,
            "expected_offset_bits": round(expected_offset_bits, 2),
            "address_minus_data_bits": round(expected_offset_bits - raw_bits, 4),
            "findable_within_N": idx >= 0,
            "first_occurrence_in_window": (idx if idx >= 0 else None),
        })

    report = {
        "title": "π as the Kolmogorov specimen",
        "provenance": {
            "real_inputs": [
                f"{N_DIGITS} decimal digits of π via Gibbons spigot (verified prefix)",
                "all byte counts, χ², and offsets measured",
            ],
            "designed_choices": [
                f"N_DIGITS={N_DIGITS}, zstd level {ZSTD_LEVEL}",
                "offset-search target '20260616'",
            ],
            "not_claimed": [
                "π normality is conjectured, not proven; results hold for the "
                "generated prefix as measured",
            ],
        },
        "looks_random": {
            "ascii_bytes": len(ascii_bytes),
            "zstd_bytes": len(z),
            "zstd_ratio": round(len(ascii_bytes) / len(z), 3),
            "symbol_entropy_floor_bytes": entropy_min_bytes,
            "zstd_near_entropy_floor": abs(len(z) - entropy_min_bytes) / entropy_min_bytes < 0.1,
            "chi_square": round(chi2, 3),
            "chi_square_5pct_critical": 16.919,
            "passes_uniformity": chi2 < 16.919,
        },
        "generator_aware": {
            "recipe_bytes": recipe_bytes,
            "lossless": bool(lossless),
            "ratio_vs_ascii": round(len(ascii_bytes) / recipe_bytes, 1),
            "beats_zstd_by": round(len(z) / recipe_bytes, 1),
        },
        "pi_as_store_fails": {
            "explanation": "the address IS the data: log2(10^L) = L*log2(10), so "
                           "encoding the offset of an L-digit string costs exactly "
                           "the information content of the data. No net compression "
                           "at any length; plus you must also store L.",
            "target": target,
            "by_length": offsets,
            "address_ever_cheaper_than_data": any(
                o["address_minus_data_bits"] < -1e-9 for o in offsets),
        },
        "convergence_record": {
            "hypothesis": "π digits look random to any byte-level codec yet are "
                          "Kolmogorov-trivial; and π cannot be used to compress "
                          "arbitrary data (offset length ≈ data length).",
            "evidence": f"zstd {round(len(ascii_bytes)/len(z),2)}× (≈symbol-entropy "
                        f"floor), χ²={round(chi2,1)}<16.9 (uniform); recipe "
                        f"{recipe_bytes} B regenerates all {N_DIGITS} digits "
                        f"(lossless={lossless}), beating zstd by "
                        f"{round(len(z)/recipe_bytes,1)}×; offset search shows "
                        f"no length saves bits.",
            "result": "apparent entropy ≠ Kolmogorov complexity; no free lunch — "
                      "π is a generator, not a universal store.",
            "confidence": "observable 1.0 (measured); π normality conjectured.",
            "sources": ["experiments/sigma0_pi_kolmogorov.py", ARTIFACT],
        },
    }

    os.makedirs(os.path.dirname(ARTIFACT), exist_ok=True)
    with open(ARTIFACT, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2)

    # ── human-readable ──
    lr, ga, fail = report["looks_random"], report["generator_aware"], report["pi_as_store_fails"]
    print(f"π — the Kolmogorov specimen   ({N_DIGITS} digits, prefix {prefix}…)")
    print()
    print("1. π LOOKS random to a byte-level codec:")
    print(f"   ascii            = {lr['ascii_bytes']:,} B")
    print(f"   zstd             = {lr['zstd_bytes']:,} B  ({lr['zstd_ratio']}×) "
          f"— only the 10-symbol entropy floor ({lr['symbol_entropy_floor_bytes']:,} B)")
    print(f"   χ² uniformity    = {lr['chi_square']}  (<16.9 ⇒ passes: {lr['passes_uniformity']})")
    print()
    print("2. π IS generated by ~nothing (Kolmogorov):")
    print(f"   recipe           = {ga['recipe_bytes']} B  regenerates all "
          f"{N_DIGITS} digits losslessly ({ga['lossless']})")
    print(f"   ratio vs ascii   = {ga['ratio_vs_ascii']}×   beats zstd by {ga['beats_zstd_by']}×")
    print()
    print("3. but π is NOT a universal store (no free lunch):")
    print(f"   {'L':>2} | {'data bits':>9} | {'address bits':>12} | {'addr−data':>9} | findable?")
    print("   " + "-" * 56)
    for o in fail["by_length"]:
        print(f"   {o['length_digits']:>2} | {o['raw_data_bits']:>9} | "
              f"{o['expected_offset_bits']:>12} | {o['address_minus_data_bits']:>9} | "
              f"{'yes' if o['findable_within_N'] else 'no (need ~10^L digits)'}")
    print(f"   address ever cheaper than data? {fail['address_ever_cheaper_than_data']}")
    print("   → log2(10^L) = L·log2(10): the pointer carries exactly as much")
    print("     information as the data. That identity IS the no-free-lunch theorem.")
    print(f"\nartifact: {ARTIFACT}")


if __name__ == "__main__":
    main()
